/**
 * @brief Method for sending Quote to BC
 * @param stockName: the stock under operation, orderID: a uuid for each order, price, shareSize, orderType: order items, username: who send the order
 * @return None
 */
void SHIFTServiceHandler::submitOrder(const std::string& stockName, const std::string& orderID, const double price, const int32_t shareSize, const std::string& orderType, const std::string& username)
{
    shift::Order::Type type = shift::Order::Type(orderType[0]);
    // if the orderID is empty it is going to be automatically generated by the constructor of Order
    shift::Order order(stockName, price, shareSize, type);
    if (orderID != "") {
        order.setID(orderID);
    }
    shift::CoreClient* ccptr = shift::FIXInitiator::getInstance().getClientByName(username);
    if (ccptr)
        ccptr->submitOrder(order);
}

/**
 * @brief Method for sending current username to frontend
 * @param username: the current user who is operating on WebClient
 * @return None
 */
void SHIFTServiceHandler::webUserLogin(const std::string& username)
{
    if (username == "")
        return;

    try {
        shift::FIXInitiator::getInstance().getClientByName(username);
        shift::FIXInitiator::getInstance().webClientSendUsername(username);
    } catch (...) {
        shift::CoreClient* ccptr = new UserClient(username);
        shift::FIXInitiator::getInstance().attach(ccptr);
    }
}

